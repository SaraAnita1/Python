# Sáo operadores utilizados para comparação de valores 

 #Igualdade

saldo = 450 
saque = 200

# = = -> igual
print (saldo == saque)

    #Retorno sempre em Booleano

# ! = -> diferente
print (saldo != saldo)

# > -> maior que 
print (saldo > saque)

# > = Maior igual a 
print (saque >= saldo)

# < -> menor que
print (saldo < saque)

# < = Menor igual a 
print (saldo <= saque)


